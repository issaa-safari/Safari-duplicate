-- ── RLS: admin-manage only (public/token reads go through service role) ───
alter table booking_links enable row level security;
alter table hotel_vouchers enable row level security;
alter table proposal_templates enable row level security;

drop policy if exists "Admins can manage booking_links" on booking_links;
create policy "Admins can manage booking_links" on booking_links
  for all to authenticated
  using (public.is_admin_user())
  with check (public.is_admin_user());

drop policy if exists "Admins can manage hotel_vouchers" on hotel_vouchers;
create policy "Admins can manage hotel_vouchers" on hotel_vouchers
  for all to authenticated
  using (public.is_admin_user())
  with check (public.is_admin_user());

drop policy if exists "Admins can manage proposal_templates" on proposal_templates;
create policy "Admins can manage proposal_templates" on proposal_templates
  for all to authenticated
  using (public.is_admin_user())
  with check (public.is_admin_user());
