-- Group 69: Booking links, hotel vouchers, and the editable proposal template
--
-- Three independent additions that share one migration group:
--
--   * booking_links      — a token-linked, shareable self-service booking form
--                          for a specific departure. Send the link over WhatsApp;
--                          the recipient fills their traveller details and the
--                          booking lands in `bookings`/`booking_travellers`
--                          exactly like a website booking (reuses the existing
--                          /api/departures/:id/book pipeline).
--
--   * hotel_vouchers     — one accommodation-booking voucher per hotel stay on a
--                          confirmed departure. Generated by walking the tour's
--                          itinerary (tour_days.accommodation_id), grouping
--                          consecutive nights at the same hotel into a single
--                          stay with check-in/check-out dates, room count and
--                          guest names. Each carries a public token so the hotel
--                          can view/print it, and an emailable status workflow
--                          (draft → sent → confirmed) — mirrors the
--                          traveller_agreements token/e-sign pattern (group_66).
--
--   * proposal_templates — the editable cover letter + client email that goes
--                          out with a quote proposal. Bilingual EN/AR, with
--                          [Placeholder] tokens the app substitutes at send
--                          time. Mirrors agreement_templates (group_66): a
--                          single active row edited in the admin back office.
--
-- Public read on all three happens server-side through the service-role client
-- (createAdminClient), which bypasses RLS — the same pattern the agreement and
-- quote token pages already use — so RLS here only needs an admin-manage gate.
--
-- Idempotent — safe to re-run. Run after group_68.

-- ── Booking links ─────────────────────────────────────────────────────────
create table if not exists booking_links (
  id uuid primary key default gen_random_uuid(),
  departure_id uuid not null references departures(id) on delete cascade,
  token uuid unique not null default gen_random_uuid(),
  label text,                                     -- e.g. "WhatsApp enquiry — Ahmed"
  language text not null default 'en' check (language in ('en', 'ar')),
  max_bookings integer check (max_bookings is null or max_bookings > 0),
  use_count integer not null default 0 check (use_count >= 0),
  expires_at timestamptz,
  is_active boolean not null default true,
  created_by uuid,                                -- auth.users id of the admin
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_booking_links_departure_id on booking_links(departure_id);
create index if not exists idx_booking_links_token on booking_links(token);

drop trigger if exists update_booking_links_updated_at on booking_links;
create trigger update_booking_links_updated_at before update on booking_links
  for each row execute function update_updated_at_column();

-- ── Hotel vouchers ────────────────────────────────────────────────────────
-- Human-friendly, monotonic voucher numbers: HV-000001, HV-000002, …
create sequence if not exists hotel_voucher_number_seq;

create table if not exists hotel_vouchers (
  id uuid primary key default gen_random_uuid(),
  voucher_number text unique not null
    default 'HV-' || lpad(nextval('hotel_voucher_number_seq')::text, 6, '0'),
  token uuid unique not null default gen_random_uuid(),

  -- What this voucher belongs to. departure_id is the usual anchor; booking_id
  -- and quote_id are optional links for provenance/reporting.
  departure_id uuid references departures(id) on delete cascade,
  booking_id uuid references bookings(id) on delete set null,
  quote_id uuid references quotes(id) on delete set null,
  accommodation_id uuid references accommodations(id) on delete set null,

  -- Snapshotted hotel identity, so the voucher stays intact even if the
  -- accommodation record is later edited or removed.
  hotel_name text not null,
  hotel_email text,

  -- Stay details.
  check_in date not null,
  check_out date not null,
  nights integer not null check (nights > 0),
  num_rooms integer not null default 1 check (num_rooms > 0),
  room_type text,
  num_guests integer not null default 1 check (num_guests > 0),
  guest_names jsonb not null default '[]'::jsonb,   -- ["Ahmed Ali", "Sara Ali"]
  meal_plan text,                                    -- e.g. "Bed & Breakfast"
  special_requests text,
  internal_notes text,

  -- Workflow.
  status text not null default 'draft'
    check (status in ('draft', 'sent', 'confirmed', 'cancelled')),
  language text not null default 'en' check (language in ('en', 'ar')),
  hotel_confirmation_ref text,                       -- hotel's own booking ref
  sent_at timestamptz,
  confirmed_at timestamptz,

  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_hotel_vouchers_departure_id on hotel_vouchers(departure_id);
create index if not exists idx_hotel_vouchers_accommodation_id on hotel_vouchers(accommodation_id);
create index if not exists idx_hotel_vouchers_status on hotel_vouchers(status);
create index if not exists idx_hotel_vouchers_token on hotel_vouchers(token);

drop trigger if exists update_hotel_vouchers_updated_at on hotel_vouchers;
create trigger update_hotel_vouchers_updated_at before update on hotel_vouchers
  for each row execute function update_updated_at_column();

-- ── Proposal template (editable cover letter + client email) ──────────────
create table if not exists proposal_templates (
  id uuid primary key default gen_random_uuid(),
  title text not null default 'Default proposal template',

  -- Cover letter shown at the top of the client-facing proposal.
  cover_intro_en text,
  cover_intro_ar text,

  -- Email that carries the proposal link.
  email_subject_en text,
  email_subject_ar text,
  email_message_en text,
  email_message_ar text,
  email_signature_en text,
  email_signature_ar text,

  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

drop trigger if exists update_proposal_templates_updated_at on proposal_templates;
create trigger update_proposal_templates_updated_at before update on proposal_templates
  for each row execute function update_updated_at_column();

